// Write a program to compare two integers using all relational operators (==, !=, >, <, >=, <=) and display the results.

class Relational_Operators{

	public static void main(String args []){
	
		int a = 40;
		int b = 50;
		
		System.out.println(a == b);
		System.out.println(a > b);
		System.out.println(a >= b);
		System.out.println(a < b);
		System.out.println(a <= b);
		System.out.println(a != b);
	
	
	
	}

}

